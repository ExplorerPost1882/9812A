void normalize ()
{
	while(SensorValue[PotenR] > 3444)
	{
		motor[Shoulder1] = 150;
		motor[Shoulder2] = 150;
		motor[Elbow] = 0;
	}
	motor[Shoulder1] = 0;
	motor[Shoulder2] = 0;

	while(SensorValue[PotenR] < 3444)
	{
		motor[Shoulder1] = -150;
		motor[Shoulder2] = -150;
		motor[Elbow] = 0;
	}
	motor[Shoulder1] = 0;
	motor[Shoulder2] = 0;

	while(SensorValue[PotenB] < 0)
	{
		motor[Elbow] = -90;
	}
	motor[Elbow] = 0;

	while(SensorValue[PotenB] > 0)
	{
		motor[Elbow] = 90;
	}
	motor[Elbow] = 0;
}


