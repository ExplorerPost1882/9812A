void turn90right ()
{
	SensorValue[EncoR1] = 0;

	while(SensorValue(EncoR1) < 250)
	{
		motor[RightWheel2] = -90;
		motor[LeftWheel2] = 90;
	}
	motor[RightWheel2] = 0;
	motor[LeftWheel2] = 0;
}

void turn90left ()
{
	SensorValue[EncoL1] = 0;

	while(SensorValue(EncoL1) > -210)
	{

		motor[RightWheel2] = 90;
		motor[LeftWheel2] = -90;
	}
	motor[RightWheel2] = 0;
	motor[LeftWheel2] = 0;

}
void moveforwardRotations (float howMany)
{
	SensorValue[EncoL1] = 0;

	while(SensorValue(EncoL1) < howMany)
	{

		motor[RightWheel2] = 127;
		motor[LeftWheel2] = 127;
	}
	motor[RightWheel2] = 0;
	motor[LeftWheel2] = 0;

}
void movebackwardRotations (float howMany)
{
	SensorValue[EncoR1] = 0;

	while(SensorValue(EncoR1) < howMany)
	{

		motor[RightWheel2] = 90;
		motor[LeftWheel2] = 90;
	}
	motor[RightWheel2] = 0;
	motor[LeftWheel2] = 0;

}
void moveforward (int cm)
{
		while(SensorValue[UltS1] > cm)
	{
		motor[RightWheel2] = 63;
		motor[LeftWheel2] = 63;
	}
	motor[RightWheel2] = 0;
	motor[LeftWheel2] = 0;
}

void movebackward (int cm)
{
	while(SensorValue[UltS1] > cm)
	{
		motor[RightWheel2] = -63;
		motor[LeftWheel2] = -63;
	}
	motor[RightWheel2] = 0;
	motor[LeftWheel2] = 0;
}

